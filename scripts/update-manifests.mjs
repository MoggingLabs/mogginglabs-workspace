#!/usr/bin/env node
// Regenerate the install manifests (winget + homebrew cask) from built release
// artifacts — ONE command after a release build (Phase-6/02):
//
//   node scripts/update-manifests.mjs [dir]     (default: dist/)
//
// For a published release, point it at downloaded assets:
//   gh release download v0.3.0 -D /tmp/rel && node scripts/update-manifests.mjs /tmp/rel
//
// The templates live HERE so the committed manifests can never drift from the
// generator. URLs point ONLY at official GitHub release artifacts; hashes are
// computed from the actual bytes, never hand-typed. GitHub replaces spaces with
// dots in uploaded asset names — the URL basenames below do the same.
import { createHash } from 'node:crypto'
import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const REPO_URL = 'https://github.com/MoggingLabs/mogginglabs-workspace'
const dir = process.argv[2] || 'dist'
if (!existsSync(dir)) {
  console.error(`update-manifests: no such directory: ${dir}`)
  process.exit(1)
}

const files = readdirSync(dir)
const find = (suffix) => files.find((f) => f.endsWith(suffix))
const winExe = find('-win-x64.exe')
const dmgArm = find('-mac-arm64.dmg')
const dmgX64 = find('-mac-x64.dmg')

// Version comes from the artifact names themselves (source of truth = the build),
// and every artifact must agree.
const versions = new Set(
  [winExe, dmgArm, dmgX64]
    .filter(Boolean)
    .map((f) => /-(\d+\.\d+\.\d+(?:-[\w.]+)?)-(?:win|mac)-/.exec(f)?.[1])
    .filter(Boolean)
)
if (versions.size !== 1) {
  console.error(`update-manifests: expected ONE version across artifacts, got: ${[...versions].join(', ') || 'none'}`)
  process.exit(1)
}
const version = [...versions][0]
const sha256 = (f) => createHash('sha256').update(readFileSync(join(dir, f))).digest('hex')
const assetUrl = (f) => `${REPO_URL}/releases/download/v${version}/${f.replaceAll(' ', '.')}`

const wrote = []
const emit = (path, content) => {
  mkdirSync(join(path, '..'), { recursive: true })
  writeFileSync(path, content)
  wrote.push(path)
}

// ── winget (needs the NSIS exe) ──────────────────────────────────────────────
if (winExe) {
  const hash = sha256(winExe).toUpperCase()
  emit(
    'packaging/winget/MoggingLabs.Workspace.yaml',
    `# Generated by scripts/update-manifests.mjs — edit the templates there, not here.
# yaml-language-server: $schema=https://aka.ms/winget-manifest.version.1.6.0.schema.json
PackageIdentifier: MoggingLabs.Workspace
PackageVersion: ${version}
DefaultLocale: en-US
ManifestType: version
ManifestVersion: 1.6.0
`
  )
  emit(
    'packaging/winget/MoggingLabs.Workspace.installer.yaml',
    `# Generated by scripts/update-manifests.mjs — edit the templates there, not here.
# yaml-language-server: $schema=https://aka.ms/winget-manifest.installer.1.6.0.schema.json
PackageIdentifier: MoggingLabs.Workspace
PackageVersion: ${version}
InstallerLocale: en-US
InstallerType: nullsoft
Scope: user
UpgradeBehavior: install
Installers:
  - Architecture: x64
    InstallerUrl: ${assetUrl(winExe)}
    InstallerSha256: ${hash}
ManifestType: installer
ManifestVersion: 1.6.0
`
  )
  emit(
    'packaging/winget/MoggingLabs.Workspace.locale.en-US.yaml',
    `# Generated by scripts/update-manifests.mjs — edit the templates there, not here.
# yaml-language-server: $schema=https://aka.ms/winget-manifest.defaultLocale.1.6.0.schema.json
PackageIdentifier: MoggingLabs.Workspace
PackageVersion: ${version}
PackageLocale: en-US
Publisher: MoggingLabs
PublisherUrl: https://github.com/MoggingLabs
PublisherSupportUrl: ${REPO_URL}/issues
PackageName: MoggingLabs Workspace
PackageUrl: ${REPO_URL}
License: Proprietary
LicenseUrl: ${REPO_URL}/blob/main/LICENSE
ShortDescription: Multi-pane terminal workspace for AI coding agents
Moniker: mogging
Tags:
  - ai
  - agents
  - terminal
  - developer-tools
ManifestType: defaultLocale
ManifestVersion: 1.6.0
`
  )
} else {
  console.warn(`update-manifests: SKIPPED winget — no *-win-x64.exe in ${dir}`)
}

// ── homebrew cask (needs BOTH mac dmgs) ──────────────────────────────────────
if (dmgArm && dmgX64) {
  emit(
    // Tap layout (Casks/) — both what a real tap needs and what makes
    // `brew style` treat the file as a cask (its cops key off the Casks/ dir).
    'packaging/homebrew/Casks/mogginglabs-workspace.rb',
    `# Generated by scripts/update-manifests.mjs — edit the templates there, not here.
cask "mogginglabs-workspace" do
  arch arm: "arm64", intel: "x64"

  version "${version}"
  sha256 arm:   "${sha256(dmgArm)}",
         intel: "${sha256(dmgX64)}"

  url "${REPO_URL}/releases/download/v#{version}/MoggingLabs.Workspace-#{version}-mac-#{arch}.dmg"
  name "MoggingLabs Workspace"
  desc "Multi-pane terminal workspace for AI coding agents"
  homepage "${REPO_URL}"

  auto_updates true
  depends_on macos: :big_sur

  app "MoggingLabs Workspace.app"

  zap trash: [
    "~/Library/Application Support/MoggingLabs Workspace",
    "~/Library/Application Support/MoggingLabs",
    "~/Library/Preferences/ai.mogginglabs.workspace.plist",
    "~/Library/Saved Application State/ai.mogginglabs.workspace.savedState",
  ]
end
`
  )
} else {
  console.warn(`update-manifests: SKIPPED homebrew cask — need both *-mac-arm64.dmg and *-mac-x64.dmg in ${dir}`)
}

if (!wrote.length) {
  console.error('update-manifests: nothing generated (no recognizable artifacts)')
  process.exit(1)
}
console.log(`update-manifests: v${version}\n${wrote.map((w) => `  wrote ${w}`).join('\n')}`)
